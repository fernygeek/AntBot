package AntBot;

public class Tronco {
    public int largoTronco;
    public int altoTronco;
    public int anchoTronco;

    public int getLargoTronco() {
        return largoTronco;
    }
    public void setLargoTronco(int largoTronco) {
        this.largoTronco = largoTronco;
    }
    public int getAltoTronco() {
        return altoTronco;
    }
    public void setAltoTronco(int altoTronco) {
        this.altoTronco = altoTronco;
    }
    public int getAnchoTronco() {
        return anchoTronco;
    }
    public void setAnchoTronco(int anchoTronco) {
        this.anchoTronco = anchoTronco;
    }
}
